     methods: {
          hoverGallery: function (){
               let $button = $('.boxStyleBiker .boxImage')
               $button.on('mouseenter', function(e) {
                    let $imageHover = $(this).data('id') + "_hover"
                    let $image = $(this).data('id')
                    console.log($(`#${$imageHover}`))
                    gsap.to($(`#${$imageHover}`), 1, {autoAlpha:1})
                    gsap.to($(`#${$image}`), 0.5, {autoAlpha:0}); 
               })

               $button.on('mouseleave', function(e) {
                    let $imageHover = $(this).data('id') + "_hover"
                    let $image = $(this).data('id')
                    console.log($(`#${$imageHover}`))
                    gsap.to($(`#${$imageHover}`), 1, {autoAlpha:0})
                    gsap.to($(`#${$image}`), 0.5, {autoAlpha:1}); 
               })
          }
     }
}
